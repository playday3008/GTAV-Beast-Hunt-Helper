const std = @import("std");

pub const Joaat = u32;

/// Jenkins One-at-a-Time Hash (with salt)
///
/// Reference: http://burtleburtle.net/bob/hash/doobs.html
pub fn joaatWithSalt(
    comptime str: [:0]const u8,
    comptime salt: u32,
) Joaat {
    comptime var hash: Joaat = salt;

    inline for (str) |c| {
        hash +%= comptime std.ascii.toLower(c);
        hash +%= (hash << 10);
        hash ^= (hash >> 6);
    }

    hash +%= (hash << 3);
    hash ^= (hash >> 11);
    hash +%= (hash << 15);

    return hash;
}

/// Jenkins One-at-a-Time Hash
///
/// Reference: http://burtleburtle.net/bob/hash/doobs.html
pub fn joaat(
    comptime str: [:0]const u8,
) Joaat {
    return joaatWithSalt(str, 0);
}

